// Command gentables builds the per-version HL7 value-table registry from the
// Caristix HL7 Definition API and writes client/hl7/tables/tables_gen.go.
//
// The raw Caristix JSON is vendored in-repo under cmd/gentables/caristix so a
// regeneration is reproducible offline: vendored JSON is preferred and the
// network is only hit to (re)fill a missing file. The generated Go library
// makes ZERO runtime network calls; only this generator touches the network.
//
// Usage:
//
//	go run ./cmd/gentables            # use vendored JSON, fetch what is missing
//	go run ./cmd/gentables -refresh   # re-fetch every table detail from the API
//
// The output is keyed version -> table id -> ordered code list. Only tables
// whose detail carries a non-empty entries array are emitted (a fixed value
// set to enforce); user-defined tables with no entries are skipped.
package main

import (
	"bytes"
	"encoding/json"
	"flag"
	"fmt"
	"go/format"
	"io"
	"math/rand"
	"net/http"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"sync"
	"time"
)

// versions are node-hl7's VERSIONS, in the Caristix path spelling.
var versions = []string{"2.1", "2.2", "2.3", "2.3.1", "2.4", "2.5", "2.5.1", "2.6", "2.7", "2.7.1", "2.8"}

const (
	apiBase     = "https://hl7-definition.caristix.com/v2-api/1"
	concurrency = 16
	maxRetries  = 6
)

