// Command genspec transpiles the committed TypeScript metadata (the
// hl7/metadata segment and data-type catalogs) into committed Go (*_gen.go)
// under client/hl7. The HL7 value tables are generated separately by
// cmd/gentables. genspec makes no network calls: it
// reads the vendored *.ts files and reproduces the catalog exactly, so end
// users ship a fixed catalog.
//
// Usage:
//
//	go run ./cmd/genspec -src <src/hl7> -out <client/hl7>
//
// The -src flag is required and points at the vendored hl7/ source tree; -out
// defaults to the committed output tree under client/hl7.
